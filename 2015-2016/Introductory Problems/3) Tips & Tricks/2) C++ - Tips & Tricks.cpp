/* C++ - Tips & Tricks */




#include <iostream>
using namespace std;

int main() {
	
	/* 1. Multiple Input and Assignment */
	
	// If you need to input multiple values on the same line,
	// you can use one cin statement to input all of them
	
	// Example: The input is "1 2 3", you need to assign the numbers to variables A, B, C
	
	// Declare the variables first
	int A;
	int B;
	int C;
	
	// Input all the numbers with one cin statement
	// Now, A = 1, B = 2, C = 3
	cin >> A >> B >> C;

	
	
	
    return 0;
}
