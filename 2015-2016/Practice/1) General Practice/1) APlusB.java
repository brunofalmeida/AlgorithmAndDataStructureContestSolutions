import java.util.*;

public class aplusb {
	static Scanner IN = new Scanner(System.in);
	static int N, a, b;
	public static void main(String[] args) {
		N = IN.nextInt();
		for (int i = 0; i < N; i++) {
			a = IN.nextInt();
			b = IN.nextInt();
			System.out.println(a + b);
		}
	}

}
